/*******************************************************************************
* Function Name  : EXTI2_IRQHandler
* Description    : This function handles External interrupt Line 2 request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void EXTI2_IRQHandler(void)
{
	if ( EXTI_GetITStatus(EXTI_Line2) != RESET ) 
	{
		Delay(0x18000);

		if ( EXTI_GetITStatus(EXTI_Line2) != RESET ) 
		{					
			while (EXTI_GetITStatus(EXTI_Line2) == RESET);
			EXTI_ClearITPendingBit(EXTI_Line2);
			RandPicture();
			flag = 1;
		//	count++;
		//	if (count == 10)
		//	{
		//		count = 1;
		//	}
		//	flag = 1;
		}		
	}
}

/*******************************************************************************
* Function Name  : EXTI3_IRQHandler
* Description    : This function handles External interrupt Line 3 request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void EXTI3_IRQHandler(void)
{
	if ( EXTI_GetITStatus(EXTI_Line3) != RESET ) 
	{
		Delay(0x18000);
		
		if ( EXTI_GetITStatus(EXTI_Line3) != RESET ) 
		{
		
			while (EXTI_GetITStatus(EXTI_Line3) == RESET);
			EXTI_ClearITPendingBit(EXTI_Line3);
			nnn++;
			if (nnn%2 == 1)
			{
				RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM2 , ENABLE);
	
				TIM_DeInit(TIM2);
			
				TIM_TimeBaseStructure.TIM_Period = 1000;		 					//自动重装载寄存器周期的值(计数值)
																				//累计 TIM_Period个频率后产生一个更新或者中断
				TIM_TimeBaseStructure.TIM_Prescaler = (36000 - 1);				//时钟预分频数   例如：时钟频率=72MHZ/(时钟预分频+1)
				TIM_TimeBaseStructure.TIM_ClockDivision = TIM_CKD_DIV1; 			//采样分频
				TIM_TimeBaseStructure.TIM_CounterMode = TIM_CounterMode_Up; 		//向上计数模式
				TIM_TimeBaseInit(TIM2, &TIM_TimeBaseStructure);
				TIM_ClearFlag(TIM2, TIM_FLAG_Update);							//清除溢出中断标志							//禁止ARR预装载缓冲器
				TIM_ITConfig(TIM2,TIM_IT_Update,ENABLE);
			
				TIM_Cmd(TIM2, ENABLE);
			}
			else
			{
				TIM_Cmd(TIM2, DISABLE);	
			}
		}		
	}
}

